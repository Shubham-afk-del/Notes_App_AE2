<h1 class="title">About Notes App</h1>

<div class="content">
	<p>
		Notes App is a simple website for writing and keeping plain-text notes. The idea is to give you
		one easy place to jot things down and find them again later.
	</p>
	<p>
		To help you stay organised, you can sort your notes into categories (a bit like folders) and
		then filter your notes by category, so longer lists don't get messy.
	</p>
	<p>
		We built this as a group coursework project for the LCSCI7224 Web Services module at
		Northeastern University London. It was made in the UK.
	</p>
	<p>
		Developed by <strong>Shubham, Adefolake and Vishal</strong> as part of the course. It uses SvelteKit
		for the website and a small SQLite database to store the notes.
	</p>
</div>

<h1 class="title">Help &amp; FAQ</h1>
<p class="mb-4">Quick answers to common questions about using Notes App.</p>

<details class="box">
	<summary class="has-text-weight-semibold">How do I create a note?</summary>
	<p class="mt-2">
		On the homepage, fill in the "New note" form with a title and your text, pick a category if you
		want, then click <strong>Save note</strong>.
	</p>
</details>

<details class="box">
	<summary class="has-text-weight-semibold">How do I edit a note?</summary>
	<p class="mt-2">
		Click a note on the homepage to open it, change the text in the "Edit note" form, and click
		<strong>Save changes</strong>.
	</p>
</details>

<details class="box">
	<summary class="has-text-weight-semibold">How do I delete a note?</summary>
	<p class="mt-2">Open the note and click the <strong>Delete note</strong> button.</p>
</details>

<details class="box">
	<summary class="has-text-weight-semibold">How do categories work?</summary>
	<p class="mt-2">
		Categories are like folders. Create them on the Settings page, then put notes into them. Click a
		category to see only the notes inside it.
	</p>
</details>

<details class="box">
	<summary class="has-text-weight-semibold">Can I change a note's category?</summary>
	<p class="mt-2">
		Yes. Open the note, choose a different category in the "Edit note" form, and save.
	</p>
</details>

<details class="box">
	<summary class="has-text-weight-semibold">How do I switch between day and night mode?</summary>
	<p class="mt-2">
		Go to the Settings page and choose <strong>Day</strong> or <strong>Night</strong>. Your choice
		is remembered on this device.
	</p>
</details>

<details class="box">
	<summary class="has-text-weight-semibold">What should I do if I can't log in?</summary>
	<p class="mt-2">
		Check your username and password are typed correctly else reset your password on login page. If you don't have an account yet, go to
		the Register page to make one.
	</p>
</details>
